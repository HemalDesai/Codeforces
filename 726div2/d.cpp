map<pll,int> dp;
int check(ll n,ll turn){
    if(dp.find({n,turn})!=dp.end()){
        return dp[{n,turn}];
    }
    
    int c=0;
    int f;
    for(int i=2;i*i<=n;i++){
        if(n%i==0){
            c++;
            ll p1=i;
            ll p2=n/i;
            if(p2!=p1)c++;
            f=check((p1-1)*p2,!turn);
            if(f==0){
                dp[{n,turn}]=1;
                dp[{n,!turn}]=1;
                return dp[{n,turn}];
            }
            f=check((p1)*(p2-1),!turn);
            if(f==0){
                dp[{n,turn}]=1;
                dp[{n,!turn}]=1;
                return dp[{n,turn}];
            }
            
        }
    }
    dp[{n,turn}]=0;
    dp[{n,!turn}]=0;
    return 0;


}


     
    
