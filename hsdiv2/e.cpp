    int p1=0,p2=0;
    set<int> s;
    for(int i=0;i<n;i++){
        m[a[i]]++;
    
    }
    for(int i=0;i<n;i++){
        if(s.find(a[i])==s.end()){
            s.insert(a[i]);
            if(m[a[i]]>f1){
                s1=f1;
                f1=m[a[i]];
            }
            else if(m[a[i]]>s1){
                s1=m[a[i]];
            }

        }
    }
    sort(a,a+n);
    reverse(a,a+n);
    int sum=0;
    for(int i=0;i<n;i++){
        if(f1!=0){
            if(m[a[i]]==f1){
                sum+=a[i];
                m[a[i]]=-1;
                f1=0;
            }
        }
        if(s1!=0){
            if(m[a[i]]==s1){
                sum+=a[i];
                m[a[i]]=-1;
                s1=0;
            }
        }
    }
