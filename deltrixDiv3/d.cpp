int find(int parent[],int i){
    if(parent[i]==i){
        return i;
    }
    return parent[i]=find(parent,parent[i]);
}
void Union(int parent[],int rank[],int x,int y){
    if(rank[x]<rank[y]){
        parent[x]=y;
    }
    else if(rank[x]>rank[y]){
        parent[y]=x;
    }
    else{
        parent[y]=x; //x becomes parent
        rank[x]++;
    }
 
    
}
int union_sets(int p[],int size[],int a, int b,int &ans,int &c) {
    a = find(p,a);
    b = find(p,b);
    if (a != b) {
        if (size[a] < size[b])
            swap(a, b);
        p[b] = a;
        size[a] += size[b];
        size[b]=0;
        ans=max(ans,size[a]);
        
    }
    else{
        c++;
    }
    return a;
}
