inline int add(int x, int y) {
    return ((x + y) >= MOD ? x + y - MOD : x + y);
}

inline int sub(int x, int y) {
    return ((x - y) < 0 ? x - y + MOD : x - y);
}

inline int mult(int a, int b) {
    return ((1ll * a * b) % MOD);
}


int inv(int a) {
    if (a == 1) return 1;

    int p = MOD - 2;
    int ans = 1;
    while (p) {
        if (p & 1) ans = mult(ans, a);
        a = mult(a, a);
        p >>= 1;
    }
    return ans;
}
