vector<vector<int>> adj;
int dfs(int s,vector<int> &vis,string &k){
    //cout<<s<<endl;
    vis[s]=1;
    k[s]='1';
    for(int x:adj[s]){
        if(vis[x]==0){
            dfs(x,vis,k);
        }
    }
    return 0;

}

