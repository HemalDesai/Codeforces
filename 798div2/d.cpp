int cal(int x,int y,vector<int> &a1,vector<int> &a2,int n,int m){
    if(x<0 || y<0)return INT_MAX;
    if(x>=n || y>=m)return INT_MAX;
    int p1=0,p2=0;
    for(int s:a1){
        p1=max(p1,abs(x-s));
    }
    for(int s:a2){
        p2=max(p2,abs(y-s));
    }
    return p1+p2;
}
