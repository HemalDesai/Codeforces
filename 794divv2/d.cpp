int state(int i,string &s,int &a,int &b,int &c,int &d){
    int n=s.size();
    
    if(i==n){
        if(a==0 && b==0 && c==0 && d==0)return 1;
        return 0;
    }
    if(m.find({i,a,c,d})!=m.end()){
        return m[{i,a,b,c,d}];
    }
    int f=0;
    if(s[i]=='A'){
        a--;
        f|=state(i+1,s,a,b,c,d);
        a++;
        if(i+1<n && s[i+1]=='B'){
            c--;
            f|=state(i+2,s,a,b,c,d);
            c++;

        }


    }
    else{
        b--;
        f|=state(i+1,s,a,b,c,d);
        b++;
        if(i+1<n && s[i+1]=='A'){
            d--;
            f|=state(i+2,s,a,b,c,d);
            d++;

        }

    }
    return m[{i,a,b,c,d}]=f;
}
