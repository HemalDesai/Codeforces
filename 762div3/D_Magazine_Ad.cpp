//KnightMareVoid
int chk(vector<int> &v,int k,int x){
    int c=0;
    int k1=0;
    for(int i=0;i<v.size();i++){
        if(v[i]>x)return 0;
        if(c+v[i]>x){
            k1++;
            c=v[i];
        }
        else{
            c+=v[i];
        }

    }
    if(c)k1++;
    return (k1<=k);

}
